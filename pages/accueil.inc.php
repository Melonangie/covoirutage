<h1>
    Bienvenue : Participer à un covoiturage avec le "système de covoiturage" 
</h1>
<article>
    <p>
        Le projet concerne le domaine d’optimisation des systèmes de covoiturages. Cette optimisation a pour but de mettre plusieurs personnes dans un même véhicule, réduisant ainsi le nombre de véhicules en circulation, il présente donc de nombreux intérêts à tous les niveaux :
    </p>
    <ul>
        <li>
            Sur le plan <b> individuel </b> : il présente un intérêt économique en réduisant le coût de transport et coût d’usure des véhicules. En réduisant le nombre de véhicules en circulation, il peut également permettre de réduire le temps de trajet. En voyageant à plusieurs, Il réduit également le stress des personnes covoiturées, et rend le déplacement plus agréable.
        </li>
        <li>
            Sur le plan des <b>collectivités</b> : il réduit le budget nécessaire pour la création et l’entretien des axes routiers.
        </li>
        <li>
            Sur le plan <b>environnemental</b> : il permet de réduire l’émission des gaz d’échappement.
        </li>
    </ul>
</article>
